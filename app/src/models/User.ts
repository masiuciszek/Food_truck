import mongoose, { Schema } from 'mongoose';
import { User } from './documents';

const userSchema = new Schema<User>({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 5,
  },
  gender: {
    enum: ['MALE', 'FEMALE'],
    default: 'MALE',
  },
  age: {
    type: Number,
    required: true,
  },
  tokens: [
    {
      token: {
        type: String,
        required: true,
      },
    },
  ],

  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const userModel = mongoose.model<User>('User', userSchema);

export default userModel;
